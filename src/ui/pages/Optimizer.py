                                                    expected_label = example.get("label", "")  # Positive or Negative
                                                    
                                                    if not code:
                                                        continue
                                                        
                                                    # Expected result interpretation:
                                                    # - For Negative examples: expected_is_buggy = False (should not be detected as bug)
                                                    # - For Positive examples: expected_is_buggy = True (should be detected as bug)
                                                    expected_is_buggy = True if expected_label == "Positive" else False
                                                    
                                                    # Evaluate using the prompt
                                                    evaluation_result = evaluate_prompt(model, language, selected_bug_type, seed, code, str(prompt_file))
                                                    
