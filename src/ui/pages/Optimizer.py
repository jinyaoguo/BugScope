### Measure the time and token cost of prompt optimization
def optimize_prompt(model, language, bug_type, feedback_file_name) -> Tuple[str, float, int, int]:
    start_time = time.time()
    input = PromptOptimizerInput(language, bug_type, feedback_file_name)  # Now passing example_name directly
    optimizer = PromptOptimizer(
        model_name=model,
        temperature=0,
        language=language,
        bug_type=bug_type,
        max_query_num=5,
        logger=logger
    )
    
    try:
        output: PromptOptimizerOutput = optimizer.invoke(input)
        end_time = time.time()
        elapsed_time = end_time - start_time
        if output and hasattr(output, 'is_valid_json') and output.is_valid_json:
            return output.generated_prompt, elapsed_time, optimizer.input_token_cost, optimizer.output_token_cost
        else:
            st.error("Generated prompt is not valid JSON.")
            if output:
                return output.generated_prompt, elapsed_time, optimizer.input_token_cost, optimizer.output_token_cost
            return None
    except Exception as e:
        st.error(f"Error during prompt synthesis: {str(e)}")
        st.error(traceback.format_exc())
        return None
    

def evaluate_prompt(model, language, bug_type, seed_name, function_code, prompt_file) -> Tuple[bool, str]:
    input = SliceBugDetectorInput(seed_name, function_code)
    evaluator = SliceBugDetector(
        model_name=model,
        temperature=0,
        language=language,
        bug_type=bug_type,
        max_query_num=5,
        logger=logger,
        prompt_file=prompt_file
    )
    try:
        output: SliceBugDetectorOutput = evaluator.invoke(input)
        if output is None:
            st.error("No output from intra-procedural detector.")
            return None
        return (output.is_buggy, output.explanation_str)
    
    except Exception as e:
        st.error(f"Error during evaluating prompt: {str(e)}")
        st.error(traceback.format_exc())
        return None


